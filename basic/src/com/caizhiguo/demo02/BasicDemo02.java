/*
    使用注释针对HelloWorld程序进行解释说明
*/
/*
    代码: public class BasicDemo02 {}
    解释: 在程序中声明了一个名为BasicDemo02的类
    类是Java程序中最基本的单位
 */
public class BasicDemo02 {
    /*
        代码: public static void main (String[] args) {}
        解释: 声明main方法,在程序中main方法是程序的执行入口,现阶段所有的语句代码必须写在main方法
     */
    public static void main(String[] args) {
        //这是输出语句,赋值在黑窗口中打印()中的内容
        System.out.println("HelloWorld");
    }
}
